package com.example.exam2020_certificateapp.model;

public class News {
    private String title;
    private String url;
    private int id;

    public int getId() {
        return id;
    }


    public String getTitle() {
        return title;
    }

    public String getUrl() {
        return url;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public void setId(int id) {
        this.id = id;
    }

}
